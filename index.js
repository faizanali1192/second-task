var box1 = document.getElementById('box1');
box1.onclick = function () {
    if (box1.style.backgroundColor = "pink") {
        var color = "pink"
        var canvas = new fabric.Canvas('canvas', {
            width: 700,
            height: 400,
            backgroundColor: color
        });
        canvas.renderAll();
    }
}

